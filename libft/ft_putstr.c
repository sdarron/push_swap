#include "libft.h"

void		ft_putstr(char const *s)
{
	int		i;
	char	c;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			c = s[i];
			write(1, &c, 1);
			i++;
		}
	}
}
