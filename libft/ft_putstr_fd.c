#include "libft.h"

void		ft_putstr_fd(char const *s, int fd)
{
	int		i;
	char	c;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			c = s[i];
			write(fd, &c, 1);
			i++;
		}
	}
}
